/**
 * The CPC firmware routine entered at `addr`, or `undefined` if `addr` is not a
 * jumpblock entry (outside the range, or not on a 3-byte boundary).
 */
export function firmwareLabel(addr: number): string | undefined {
  const offset = addr - JUMPBLOCK_BASE
  if (offset < 0 || offset % ENTRY_BYTES !== 0) return undefined
  return ENTRIES[offset / ENTRY_BYTES]
}
